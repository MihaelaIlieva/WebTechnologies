function firstPictureAsCode(){
    var ourParagraph=document.getElementById("imageOne");
    if(counterOne%2==0){
        ourParagraph.style.visibility="visible";
        ourParagraph.style.fontSize="100%";
        counterOne++;
    }
    else{
        ourParagraph.style.visibility="hidden";
        ourParagraph.style.fontSize="0";
        counterOne++;
    }   
}
